class Student {
    id;
    name;
    dob;
    gender;
    phone;
    hometown;
    grade;

    constructor(idInput,nameInput,dobInput,genderInput,phoneInput,hometownInput,gradeInput) {
        this.id=idInput;
        this.name=nameInput;
        this.dob=dobInput;
        this.gender=genderInput;
        this.phone=phoneInput;
        this.hometown=hometownInput;
        this.grade=gradeInput;
    }
}